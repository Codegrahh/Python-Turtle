#Draw Geometrical Shapes In Python Turtle

import time

from turtle import *
#SQUARE
time.sleep(1)
pensize(5)
fillcolor('red')
begin_fill()
forward(150)
left(90)
forward(150)
left(90)
forward(150)
left(90)
forward(150)
penup()
right(30)
forward(80)
end_fill()
pendown()
write('SQUARE',font=('arial',45))
time.sleep(2)
clear()

#CIRCLE
penup()
back(80)
pendown()
time.sleep(2)
fillcolor('red')
begin_fill()
circle(100)
end_fill()
penup()
left(45)
backward(55)
pendown()
write("CIRCLE",font=("arial",45))
time.sleep(2)
clear()

